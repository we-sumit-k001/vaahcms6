            $data['fillable']['except'] = Practice::getUnFillableColumns();
            $data['empty_item'] = Practice::getEmptyItem();
            $data['taxonomy_training_data']=Practice::getTaxonomyTraining();
            $data['taxonomy_training_tag']=Practice::getTaxonomyTrainingTag('training');
            $data['urls']['upload'] = route('vh.backend.media.upload');

            $data['actions'] = [];

            $response['success'] = true;

            $response['data'] = $data;

        }catch (\Exception $e){
            $response = [];
            $response['success'] = false;
            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'] = $e->getTrace();
            } else{
                $response['errors'][] = 'Something went wrong.';
            }
        }

        return $response;
    }

